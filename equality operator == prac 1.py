#THE EQUALITY OPERATOR ==

#assigning 0 the variable
var = 0
print(var == 0)

#assigning 1 to the variable
var = 1
print(var == 0)

#THE INEQUALITY OPERATOR !=
#the answer is false if operands are equal and true if not equal
var = 0
print(var != 0)

var = 1
print(var != 0)

#THE COMPARISON OPERATOR GREATER THAN >
var1 = 100
var2 = 10
print(var1 > var2)
print(var2 > var1)

#THE COMPARISON OPERATOR GREATER THAN OR EQUAL TO >=
var3 = 20
var4 = 23
print(var3 >= var4)
print(var4 >= var3)

#COMPARISON OPERATORS LESS THAN AND LESS THAN OR EQUAL TO < AND <=
print(var2 < var3)
print(var4 <= var1)
